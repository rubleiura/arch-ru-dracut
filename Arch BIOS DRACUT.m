# ШАГ 1: УСТАНОВКА ЯДРА, ЗАГРУЗЧИКА И DRACUT
#------------------------------------------------------------------------------
# 📦 linux-lts — ядро с долгосрочной поддержкой (~100 МБ)
# 📦 linux-lts-headers — заголовки ядра для драйверов (~15 МБ)
# 📦 linux-firmware — прошивки для оборудования (~200 МБ)
pacman -Sy --noconfirm linux-lts linux-lts-headers linux-firmware
# 📦 grub — загрузчик системы для BIOS (~5 МБ)
# 📦 grub-btrfs — интеграция снапшотов BTRFS в GRUB (~0.2 МБ)
# 📦 os-prober — обнаружение других ОС на диске (~0.1 МБ)
pacman -Sy --noconfirm grub grub-btrfs os-prober
#------------------------------------------------------------------------------
# ШАГ: ОТКЛЮЧЕНИЕ ХУКОВ MKINITCPIO
#------------------------------------------------------------------------------
# ℹ️ Зачем: Запретить pacman автоматически запускать mkinitcpio при обновлениях.
# ❗ Важно: Без этого mkinitcpio будет пересобирать образы при каждом обновлении ядра.
# ✅ Маскировка хуков через symlink на /dev/null (стандартный метод Arch).
#------------------------------------------------------------------------------
# 📋 Отключение хука установки
ln -sf /dev/null /etc/pacman.d/hooks/90-mkinitcpio-install.hook
# 📋 Отключение хука удаления
ln -sf /dev/null /etc/pacman.d/hooks/60-mkinitcpio-remove.hook
# 📦 networkmanager — управление сетевыми подключениями (~2 МБ)
# 📦 wpa_supplicant — поддержка WPA/WPA2 для WiFi (~0.8 МБ)
# 📦 wireless_tools — утилиты для беспроводных сетей (~0.2 МБ)
pacman -Sy --noconfirm networkmanager wpa_supplicant wireless_tools
# 📦 openssh — сервер и клиент SSH для удалённого доступа (~2 МБ)
pacman -Sy --noconfirm openssh
# 📦 plymouth — экран загрузки с анимацией (~3 МБ)
pacman -Sy --noconfirm plymouth
# 🔧 Включение системных служб
systemctl enable NetworkManager.service
systemctl enable grub-btrfsd.service
systemctl enable sshd.service
systemctl enable plymouth-quit-wait.service
#------------------------------------------------------------------------------
# ШАГ 2: УСТАНОВКА GRUB
#------------------------------------------------------------------------------
# 📋 Установка GRUB для BIOS
# --target=i386-pc — архитектура загрузчика для BIOS (не UEFI!)
# --recheck — принудительная перепроверка устройства
# /dev/sdx — установка в MBR всего диска, а не в раздел!
# ⚠️ Замените sdx на ваш диск (например, sda)
grub-install --target=i386-pc --recheck /dev/sdx
#------------------------------------------------------------------------------
# ШАГ 3: НАСТРОЙКА DRACUT
#------------------------------------------------------------------------------
# ℹ️ Создание конфигурации dracut для Btrfs + Resume.
# - hostonly="no" гарантирует включение resume.
# - Модуль resume добавлен явно.
#------------------------------------------------------------------------------
# 📋 Создание конфигурационного файла dracut
mkdir -p /etc/dracut.conf.d
cat > /etc/dracut.conf.d/custom.conf << 'EOF'
add_dracutmodules+=" base resume btrfs plymouth "
add_drivers+=" btrfs "
hostonly="no"
compress="zstd"
EOF
#------------------------------------------------------------------------------
# ШАГ 4: НАСТРОЙКА ПАРАМЕТРОВ ЯДРА (GRUB)
#------------------------------------------------------------------------------
# ℹ️ Получение UUID разделов для параметров ядра.
# ❗ Убедитесь, что имена разделов (sda2, sda3) соответствуют вашему диску!
# - resume=UUID обязателен для гибернации.
#------------------------------------------------------------------------------
# 📋 Получение UUID swap раздела
SWAP_UUID=$(blkid -s UUID -o value /dev/sda3)
# 📋 Получение UUID root раздела
ROOT_UUID=$(blkid -s UUID -o value /dev/sda2)
# 📋 Настройка GRUB_CMDLINE_LINUX_DEFAULT
sed -i 's|^GRUB_CMDLINE_LINUX_DEFAULT=.*|GRUB_CMDLINE_LINUX_DEFAULT="loglevel=3 quiet splash rd.shell=0 rd.emergency=halt"|' /etc/default/grub
# 📋 Настройка GRUB_CMDLINE_LINUX
# ✅ ИСПРАВЛЕНО: Рабочая команда sed
sed -i "s|^[[:space:]]*#\?GRUB_CMDLINE_LINUX=.*|GRUB_CMDLINE_LINUX=\"root=UUID=${ROOT_UUID} rootflags=subvol=@ resume=UUID=${SWAP_UUID}\"|" /etc/default/grub
# 📋 Настройка модулей GRUB
sed -i 's|^GRUB_PRELOAD_MODULES=.*|GRUB_PRELOAD_MODULES="part_gpt part_msdos"|' /etc/default/grub
# 📋 Отключение режима восстановления
sed -i 's|^#GRUB_DISABLE_RECOVERY=.*|GRUB_DISABLE_RECOVERY=true|' /etc/default/grub
# #------------------------------------------------------------------------------
# ШАГ 5: НАСТРОЙКА GRUB-BTRFS И ГЕНЕРАЦИЯ
# #------------------------------------------------------------------------------
# 📋 Настройка отображения снапшотов в GRUB
sed -i 's/#GRUB_BTRFS_SUBMENUNAME=.*/GRUB_BTRFS_SUBMENUNAME="Arch Linux snapshots"/' /etc/default/grub-btrfs/config
sed -i 's/#GRUB_BTRFS_TITLE_FORMAT=.*/GRUB_BTRFS_TITLE_FORMAT=("description" "date")/' /etc/default/grub-btrfs/config
#------------------------------------------------------------------------------
# ШАГ 6: ГЕНЕРАЦИЯ КОНФИГУРАЦИИ И INITRAMFS
#------------------------------------------------------------------------------
# 📋 Генерация initramfs через dracut
dracut --force /boot/initramfs-linux-lts.img
# 📋 Генерация конфигурации GRUB
grub-mkconfig -o /boot/grub/grub.cfg
